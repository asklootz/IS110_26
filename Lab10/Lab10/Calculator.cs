namespace Lab10
{
    public class Calculator
    {
        public double AddNumbers(int tall1, int tall2)
        {
            return tall1 + tall2;
        }

        public double SubtractNumbers(int tall1, int tall2)
        {
            return tall1 - tall2;
        }

        public double MultiplyNumbers(int tall1, int tall2)
        {
            return tall1 * tall2;
        }

        public double DivideNumbers(int tall1, int tall2)
        {
            return tall1 / tall2;
        }

    }

}